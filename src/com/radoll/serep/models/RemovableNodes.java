package com.radoll.serep.models;

import javafx.scene.Node;

public class RemovableNodes {
    private Node track;
    private Node dock;
    private Node code;

    public Node getTrack() {
        return track;
    }

    public void setTrack(Node track) {
        this.track = track;
    }

    public Node getDock() {
        return dock;
    }

    public void setDock(Node dock) {
        this.dock = dock;
    }


    public Node getCode() {
        return code;
    }

    public void setCode(Node code) {
        this.code = code;
    }
}
